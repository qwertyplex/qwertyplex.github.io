// Mute or unmute the music
function MuteMusic() {
    if (document.getElementById("muteCheckbox").checked) {
        StopMusic();  // Stop any currently playing music
        player.music_muted_flag = true;
        SetVolume(0);  // Mute music
    } else {
        player.music_muted_flag = false;
        SetVolume(1);  // Unmute music (set to normal volume)
    }
}

function StopMusic() {
    // Create an array of all the music elements.
    const allMusicTracks = [
        opening_music,
        trader_music,
        lemontree_music,
        vendingmachine_music,
        caves_music,
        pawnshop_music,
        library_music,
        spookywoods_music,
        mines_music,
		factory_music,
		tunnels_music,
        temple_music,
        laundromat_music,
        computer_music,
        teahouse_music,
		teahouse_mixtape_music,
        newsstudio_music,
        bank_music,
        banquet_music,
        castle_music,
        wizard_music,
		smith_music,
        dungeon_music,
        devroom_music
    ];

    // Loop through each track and stop it if it's playing.
    allMusicTracks.forEach((music) => {
        if (music) {
            // Check if the music is playing and not muted (i.e. volume > 0)
            if (!music.paused && music.volume > 0) {
                music.pause();        // Pause the track
            }
        }
    });

    // Set the player state to reflect that no music is playing.
    player.music_is_playing = false;
}



let opening_music = document.getElementById("opening_music");
function OpeningMusic() {
    if (!player.music_muted_flag) {
        StopMusic(); // Stops the currently playing music (if any)
        opening_music.loop = true;
        opening_music.play();
        player.music_is_playing = true;
    }
}

